"""
RAGAS evaluation harness for WorkerShield v1.

Runs faithfulness, answer relevancy, and context precision metrics
against a sample of WorkerShield query/answer pairs.
"""

from ragas import evaluate
from ragas.metrics import faithfulness, answer_relevancy, context_precision
from datasets import Dataset

# RAGAS judge: local Ollama mistral (zero API cost)
# Embeddings: local nomic-embed-text via Ollama on Windows host
from langchain_ollama import ChatOllama, OllamaEmbeddings
from ragas.llms import LangchainLLMWrapper
from ragas.embeddings import LangchainEmbeddingsWrapper

ollama_llm = ChatOllama(
    model="mistral",
    base_url="http://192.168.100.10:11434"
)
judge_llm = LangchainLLMWrapper(ollama_llm)

ollama_embeddings = OllamaEmbeddings(
    model="nomic-embed-text",
    base_url="http://192.168.100.10:11434"
)
judge_embeddings = LangchainEmbeddingsWrapper(ollama_embeddings)

# ---------------------------------------------------------------------------
# Sample evaluation dataset
# Each row: question, answer generated by WorkerShield, retrieved contexts
# ---------------------------------------------------------------------------
eval_data = {
    "question": [
        "What are an employer's duties under the Queensland WHS Act?",
        "When is a casual employee eligible for conversion to permanent?",
        "My FIFO worker has a mental health condition and wants to reduce hours — "
        "what are my obligations under safety law and fair work?",
    ],
    "answer": [
        "placeholder — replace with live agent output",
        "placeholder — replace with live agent output",
        "placeholder — replace with live agent output",
    ],
    "contexts": [
        ["placeholder context chunk 1", "placeholder context chunk 2"],
        ["placeholder context chunk 1", "placeholder context chunk 2"],
        ["placeholder context chunk 1", "placeholder context chunk 2"],
    ],
    "ground_truth": [
        "placeholder ground truth",
        "placeholder ground truth",
        "placeholder ground truth",
    ],
}

dataset = Dataset.from_dict(eval_data)

result = evaluate(
    dataset=dataset,
    metrics=[faithfulness, answer_relevancy, context_precision],
    llm=judge_llm,
    embeddings=judge_embeddings,
)

print(result)
